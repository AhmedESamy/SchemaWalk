def load_graph(name):
    graph = networkx.read_edgelist(f'data/{name}/edgelist.tsv', delimiter = '\t')

    edge_types = {e: {'type': ''.join(sorted([e[0][0], e[1][0]]))} for e in graph.edges}
    networkx.set_edge_attributes(graph, edge_types)

    return graph

def load_labels(name):
    data = pandas.read_csv(f'data/{name}/labels.tsv', sep = '\t', header = None)

    nodes = data.iloc[:,0].to_list()
    labels = data.iloc[:,1:].to_numpy()
    if labels.shape[-1] == 1:
        labels = LabelBinarizer().fit_transform(labels)

    return nodes, labels
